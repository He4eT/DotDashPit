const playerStates = {
  default: {
    speed: 1,
    sprite: 256,
  },
  dot: {
    speed: 1.5,
    sprite: 257,
  },
  dash: {
    speed: 2,
    sprite: 258,
  },
}

function handleMoves() {
  let dx = 0
  if (btn(BTN_L)) dx -= 1
  if (btn(BTN_R)) dx += 1

  let dy = 0
  if (btn(BTN_U)) dy -= 1
  if (btn(BTN_D)) dy += 1

  const norm =
    playerStates[player.state].speed /
    ([dx, dy].every((d) => d !== 0) ? Math.SQRT2 : 1)

  player.position = {
    x: clamp(player.position.x + dx * norm)(
      arena.bounds.left,
      arena.bounds.right,
    ),
    y: clamp(player.position.y + dy * norm)(
      arena.bounds.top,
      arena.bounds.bottom,
    ),
  }
}

function handleMorse() {
  const { key } = player
  const now = time()

  const buttonPressed = [BTN_A, BTN_B, BTN_X, BTN_Y].some(btn)

  /* Down */
  if (buttonPressed && !key.isDown) {
    key.isDown = true
    key.downAt = now
  }

  /* Hold */
  if (buttonPressed && key.isDown) {
    const isDash = now - key.downAt > DOT_DASH_THRESHOLD
    player.state = isDash ? 'dash' : 'dot'
    playMorseKey(arena.waveSeed)
  }

  /* Release */
  if (!buttonPressed && key.isDown) {
    player.state = 'default'
    key.isDown = false
    key.upAt = now
    key.buffer += key.upAt - key.downAt < DOT_DASH_THRESHOLD ? '.' : '-'

    effects.push({
      type: 'detection',
      frames: [15],
      to: player.position,
    })
  }

  /* Flush */
  if (
    !buttonPressed &&
    key.buffer.length > 0 &&
    now - key.upAt > DOT_DASH_IDLE_TIMEOUT
  ) {
    if (morseToLetter[key.buffer]) {
      const letter = morseToLetter[key.buffer]
      destroyEnemiesByLetter(letter)
      key.history += letter
    } else {
      key.history += ' '
    }
    key.buffer = ''
    key.history = key.history.slice(-1 * HISTORY_LENGTH)
  }
}

function drawPlayer() {
  drawSprite(
    playerStates[player.state].sprite,
    player.position.x,
    player.position.y,
  )
}

