/* Game Over */

function gameoverScreen() {
  cls(0)
  map(0, 0, 30, 17)
  drawFX()
  drawEnemies()
  drawPlayer()

  if (effects.length === 0) {
    print(
      player.score.toString().padStart(SCORE_LENGTH),
      SCORE_X,
      SCORE_Y,
      6,
      true,
    )
    print(
      player.score > arena.highscore ? 'NEW HIGH SCORE!' : 'GAME OVER',
      HISTORY_X,
      HISTORY_Y,
      player.score > arena.highscore ? 9 : 10,
      false,
    )

    if (anyKeyPressed()) {
      reset()
    }
  }
}

function gameover() {
  effects = [
    {
      type: 'flash',
      frames: '77777777777765432'.split(''),
    },
  ]

  if (player.score > arena.highscore) {
    pmem(0, player.score)
  }

  currentScreen = 'gameoverScreen'
}

/* Utils */

function arr(n, filler) {
  const result = []
  for (let i = 0; i < n; i++) {
    result.push(filler)
  }
  return result
}

function clamp(value) {
  return (min, max) => Math.max(min, Math.min(max, value))
}

function rnd(from, to) {
  return Math.floor(Math.random() * (to - from + 1)) + from
}

function getDistance(from, to) {
  return Math.hypot(from.x - to.x, from.y - to.y)
}

function getDirection(from, to) {
  const dx = to.x - from.x
  const dy = to.y - from.y
  const distance = Math.hypot(dx, dy) || 1

  return {
    x: dx / distance,
    y: dy / distance,
  }
}

function worldToScreen({ x, y }) {
  return {
    x: Math.floor(x + arena.screenPosition.x),
    y: Math.floor(y + arena.screenPosition.y),
  }
}

function drawSprite(spriteIndex, x, y) {
  const colorkey = 0
  const center = worldToScreen({ x, y })

  spr(spriteIndex, center.x - SPRITE_HALF, center.y - SPRITE_HALF, colorkey)
}

function anyKeyPressed() {
  return arr(8)
    .map((_, i) => i)
    .some(btnp)
}

